int	cub_init(t_cub *cub, char **av)
{
	cub->ray = malloc(sizeof(t_ray));
	if (!cub->ray)
		return (1);
	cub->matrix = NULL;
	cub->map = ft_strdup(av[1]);
	cub->img = NULL;
	cub->mlx = NULL;
	cub_init_matrix(cub);
	if (cub_player_init(cub))
		return (1);
	return (0);
}

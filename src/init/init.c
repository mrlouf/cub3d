static int	cub_player_init(t_cub *cub)
{
	cub->player = malloc(sizeof(t_player));
	if (!cub->player)
		return (1);
	cub->player->pos.x = 22;
	cub->player->pos.y = 11;
	cub->player->dir.x = -1;
	cub->player->dir.y = 0;
	cub->player->plane.x = 0;
	cub->player->plane.y = 0.66;
	cub->player->speed = 0.10;
	cub->player->rotation = 0.05;
	cub->player->angle = atan2(cub->player->dir.y, cub->player->dir.x);
	return (0);
}

/*
	Allocates pointers for the minimap matrix.
	!Currently copies manually the test map defined in the file,
	!but it will need to be modified to accommodated any other map read.
*/
static int	cub_init_minimap(t_cub *cub)
{
	int	i;
	int	j;

	cub->map = ft_calloc(sizeof(int *), 24 + 1);
	if (!cub->map)
		return (1);
	i = -1;
	while (++i < 25)
	{
		cub->map[i] = ft_calloc(sizeof(int), 24 + 1);
		if (!cub->map[i])
		{
			ft_free_narray((void **)cub->map, i);
			return (1);
		}
	}
	i = -1;
	while (++i < 24)
	{
		j = -1;
		while (++j < 24)
			cub->map[i][j] = g_map[i][j];
	}
	return (0);
}

int	cub_init(t_cub *cub, char **av)
{
	cub->map = NULL;
	cub->filename = ft_strdup(av[1]);
	if (!cub->filename)
		return (1);
	cub->img = NULL;
	cub->mlx = NULL;
	cub->icon = mlx_load_png("./src/utils/icon.png");
	cub->minimap_px = 2;
	if (cub_init_minimap(cub))
		return (1);
	if (cub_player_init(cub))
		return (1);
	return (0);
}
